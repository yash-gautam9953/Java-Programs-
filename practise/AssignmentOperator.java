class AssignmentOperator{

	public static void main(String[] args){
		
		int a=12;
		System.out.println();
		System.out.println("A : " + a );
		System.out.println();
		System.out.println("A+=2 :" + (a+=2));
		System.out.println("A-=1 :" + (a-=1));
		System.out.println("A*=2 :" + (a*=2));
		System.out.println("A/=2 :" + (a/=2));
		System.out.println("A%=2 :" + (a%=2));
		System.out.println();

		}
	
}
class RelationalOperator{

	public static void main(String[] args){
		
		int a=13;
		int b=23;

		System.out.println();
		System.out.println("A : " + a + " B :" + b);
		System.out.println();
		System.out.println("A==B :" + (a==b));
		System.out.println("A!=B :" + (a!=b));
		System.out.println("A<B :" + (a<b));
		System.out.println("A>B :" + (a>b));
		System.out.println("A<=B :" + (a<=b));
		System.out.println("A>=B :" + (a>=b));
		System.out.println();

		}
	
}
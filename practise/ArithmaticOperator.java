class ArithmaticOperator{

	public static void main(String[] args){
		
		int a=100;
		int b=15;
		System.out.println();
		System.out.println("A : " + a + " B :" + b);
		System.out.println();
		System.out.println("A+B :" + (a+b));
		System.out.println("A-B :" + (a-b));
		System.out.println("A*B :" + (a*b));
		System.out.println("A/B :" + (a/b));
		System.out.println("A%B :" + (a%b));
		System.out.println();

		}
	
}
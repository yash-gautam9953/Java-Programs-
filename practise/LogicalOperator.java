class LogicalOperator{

	public static void main(String[] args){
		
		Boolean a = true;
		Boolean b= false;

		System.out.println();
		System.out.println("A : " + a + " B :" + b);
		System.out.println("A && B :" + (a&&b));
		System.out.println("A || B :" + (a||b));
		System.out.println("! A :" + (!a));
		System.out.println("! B :" + (!b));
		System.out.println();

		}
	
}
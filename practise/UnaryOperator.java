class UnaryOperator{

	public static void main(String[] args){
		
		int a=1;
		System.out.println();
		System.out.println("A : " + a );
		System.out.println();
		System.out.println("A++ :" + (a++));
		System.out.println("++A :" + (++a));
		System.out.println("A-- :" + (a--));
		System.out.println("--A :" + (--a));
		System.out.println();

		}
	
}